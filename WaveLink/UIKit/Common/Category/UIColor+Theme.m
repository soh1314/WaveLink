#import "UIColor+Theme.h"

@implementation UIColor (Theme)

+ (UIColor *)themeColor {
    return [UIColor colorWithHex:@"#829cfc"];
}

+ (UIColor *)themelightGrayColor {
    return [UIColor colorWithHex:@"#dcdcdc"];
}

+ (UIColor *)themeTextBlackColor {
    return [UIColor colorWithHex:@"#475061"];
}

+ (UIColor *)themeRedColor {
    return [UIColor colorWithHex:@"#fc8282"];
}

@end
